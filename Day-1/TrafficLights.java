package Training;

public class TrafficLights {

	public static void main(String[] args) {
		String color="red";
		if(color=="red")
			System.out.println("Stop");
		else if(color=="green")
			System.out.println("Go");
		else if(color=="orange")
			System.out.println("Wait");
		else
			System.out.println("Invalid input");
	}

}

